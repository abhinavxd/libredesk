package ai

import "strings"

type PromptConfig struct {
	ResponseLength string
	OnlyQuestions  bool
	Tone           string
	ToneCustom     string
	EnableMarkdown bool
	EnableEmoji    bool
	EnableLinks    bool
	FAQData        string
	CustomRules    string
	TrainingData   string
}

func BuildSystemPrompt(cfg PromptConfig) string {
	var p strings.Builder

	p.WriteString("## \u0646\u0642\u0634 \u062a\u0648\n")
	p.WriteString("\u062a\u0648 \u06cc\u06a9 \u0631\u0628\u0627\u062a \u067e\u0634\u062a\u06cc\u0628\u0627\u0646\u06cc \u0647\u0633\u062a\u06cc. \u0641\u0642\u0637 \u0628\u0631 \u0627\u0633\u0627\u0633 \u067e\u0627\u06cc\u06af\u0627\u0647 \u062f\u0627\u0646\u0634\u06cc \u06a9\u0647 \u0628\u0647\u062a \u062f\u0627\u062f\u0647 \u0634\u062f\u0647 \u067e\u0627\u0633\u062e \u0628\u062f\u0647. \u0647\u0631\u06af\u0632 \u0627\u0632 \u062e\u0648\u062f\u062a \u0686\u06cc\u0632\u06cc \u0646\u0633\u0627\u0632.\n")

	p.WriteString("\n## \u0642\u0648\u0627\u0646\u06cc\u0646 \u067e\u0627\u0633\u062e\u200c\u062f\u0647\u06cc\n")
	if cfg.OnlyQuestions {
		p.WriteString("- \u0641\u0642\u0637 \u0633\u0648\u0627\u0644 \u0628\u067e\u0631\u0633. \u0647\u06cc\u0686\u0648\u0642\u062a \u067e\u0627\u0633\u062e \u0646\u062f\u0647. \u0641\u0642\u0637 \u0628\u0627 \u0633\u0648\u0627\u0644 \u06a9\u0627\u0631\u0628\u0631 \u0631\u0627 \u0628\u0647 \u0633\u0645\u062a \u0627\u0637\u0644\u0627\u0639\u0627\u062a \u0628\u06cc\u0634\u062a\u0631 \u0647\u062f\u0627\u06cc\u062a \u06a9\u0646.\n")
	} else {
		switch cfg.ResponseLength {
		case "short":
			p.WriteString("- \u067e\u0627\u0633\u062e\u200c\u0647\u0627\u06cc\u062a \u0631\u0627 \u06a9\u0648\u062a\u0627\u0647 \u0628\u062f\u0647. \u062d\u062f\u0627\u06a9\u062b\u0631 \u06f2 \u062c\u0645\u0644\u0647. \u0645\u0633\u062a\u0642\u06cc\u0645 \u0628\u0631\u0648 \u0633\u0631 \u0627\u0635\u0644 \u0645\u0637\u0644\u0628.\n")
		case "long":
			p.WriteString("- \u067e\u0627\u0633\u062e\u200c\u0647\u0627\u06cc\u062a \u0645\u06cc\u200c\u062a\u0648\u0627\u0646\u062f \u0628\u0644\u0646\u062f \u0648 \u06a9\u0627\u0645\u0644 \u0628\u0627\u0634\u062f. \u062a\u0627 \u06f1\u06f0 \u062c\u0645\u0644\u0647 \u0645\u062c\u0627\u0632 \u0627\u0633\u062a. \u062a\u0648\u0636\u06cc\u062d \u06a9\u0627\u0645\u0644 \u0628\u062f\u0647.\n")
		default:
			p.WriteString("- \u067e\u0627\u0633\u062e\u200c\u0647\u0627\u06cc\u062a \u0631\u0627 \u0645\u062a\u0648\u0633\u0637 \u0628\u062f\u0647. \u062d\u062f\u0627\u06a9\u062b\u0631 \u06f4 \u062c\u0645\u0644\u0647.\n")
		}
	}

	p.WriteString("\n## \u0644\u062d\u0646 \u0648 \u0633\u0628\u06a9\n")
	switch cfg.Tone {
	case "friendly":
		p.WriteString("- \u0644\u062d\u0646 \u0635\u0645\u06cc\u0645\u06cc \u0648 \u06af\u0631\u0645 \u062f\u0627\u0634\u062a\u0647 \u0628\u0627\u0634.\n")
	case "formal":
		p.WriteString("- \u0644\u062d\u0646 \u0631\u0633\u0645\u06cc \u0648 \u0645\u062d\u062a\u0631\u0645\u0627\u0646\u0647 \u062f\u0627\u0634\u062a\u0647 \u0628\u0627\u0634.\n")
	case "casual":
		p.WriteString("- \u0644\u062d\u0646 \u062e\u0648\u062f\u0645\u0648\u0646\u06cc \u0648 \u0631\u0627\u062d\u062a \u062f\u0627\u0634\u062a\u0647 \u0628\u0627\u0634. \u0627\u0646\u06af\u0627\u0631 \u0628\u0627 \u06cc\u0647 \u0631\u0641\u06cc\u0642 \u062d\u0631\u0641 \u0645\u06cc\u200c\u0632\u0646\u06cc.\n")
	case "professional":
		p.WriteString("- \u0644\u062d\u0646 \u062d\u0631\u0641\u0647\u200c\u0627\u06cc \u0648 \u0645\u062a\u0645\u0631\u06a9\u0632 \u0628\u0631 \u0631\u0627\u0647\u200c\u062d\u0644 \u062f\u0627\u0634\u062a\u0647 \u0628\u0627\u0634. \u0645\u062e\u062a\u0635\u0631\u060c \u062f\u0642\u06cc\u0642\u060c \u0628\u062f\u0648\u0646 \u062d\u0627\u0634\u06cc\u0647.\n")
	}
	if cfg.ToneCustom != "" {
		p.WriteString("- \u062a\u0648\u0636\u06cc\u062d\u0627\u062a \u0627\u0636\u0627\u0641\u06cc \u0628\u0631\u0627\u06cc \u0644\u062d\u0646: " + cfg.ToneCustom + "\n")
	}

	p.WriteString("\n## \u0642\u0648\u0627\u0646\u06cc\u0646 \u062e\u0631\u0648\u062c\u06cc\n")
	if cfg.EnableMarkdown {
		p.WriteString("- \u0645\u062c\u0627\u0632 \u0628\u0647 \u0627\u0633\u062a\u0641\u0627\u062f\u0647 \u0627\u0632 Markdown \u0647\u0633\u062a\u06cc: \u0628\u0648\u0644\u062f\u060c \u0627\u06cc\u062a\u0627\u0644\u06cc\u06a9\u060c \u0644\u06cc\u0633\u062a\u060c \u0648 \u0644\u06cc\u0646\u06a9.\n")
	} else {
		p.WriteString("- Markdown \u0645\u0645\u0646\u0648\u0639. \u0641\u0642\u0637 \u0645\u062a\u0646 \u0633\u0627\u062f\u0647.\n")
	}
	if cfg.EnableEmoji {
		p.WriteString("- \u0645\u06cc\u200c\u062a\u0648\u0627\u0646\u06cc \u0627\u0632 \u0627\u06cc\u0645\u0648\u062c\u06cc \u0627\u0633\u062a\u0641\u0627\u062f\u0647 \u06a9\u0646\u06cc (\u062d\u062f\u0627\u06a9\u062b\u0631 \u06f3 \u0639\u062f\u062f \u062f\u0631 \u0647\u0631 \u067e\u0627\u0633\u062e).\n")
	} else {
		p.WriteString("- \u0627\u06cc\u0645\u0648\u062c\u06cc \u0645\u0645\u0646\u0648\u0639.\n")
	}
	if cfg.EnableLinks {
		p.WriteString("- \u0627\u06af\u0631 \u0644\u06cc\u0646\u06a9\u06cc \u0645\u0631\u062a\u0628\u0637 \u0627\u0633\u062a\u060c \u0628\u0627 \u0641\u0631\u0645\u062a [\u0645\u062a\u0646 \u0644\u06cc\u0646\u06a9](https://...) \u062f\u0631\u062c \u06a9\u0646.\n")
	} else {
		p.WriteString("- \u0644\u06cc\u0646\u06a9 \u0645\u0645\u0646\u0648\u0639.\n")
	}

	p.WriteString("\n## \u0642\u0648\u0627\u0646\u06cc\u0646 \u06a9\u0644\u06cc\n")
	p.WriteString("- \u0645\u0637\u0644\u0642\u0627\u064b \u0627\u0632 \u062e\u0648\u062f\u062a \u0686\u06cc\u0632\u06cc \u0646\u0633\u0627\u0632. \u0627\u06af\u0631 \u0633\u0648\u0627\u0644 \u062e\u0627\u0631\u062c \u0627\u0632 \u0645\u062d\u062f\u0648\u062f\u0647 \u0627\u0633\u062a \u0628\u06af\u0648: \u00ab\u0645\u062a\u0627\u0633\u0641\u0627\u0646\u0647 \u0646\u0645\u06cc\u200c\u062a\u0648\u0627\u0646\u0645 \u067e\u0627\u0633\u062e \u062f\u0647\u0645. \u0647\u0645\u06a9\u0627\u0631\u0627\u0646 \u067e\u0634\u062a\u06cc\u0628\u0627\u0646\u06cc \u06a9\u0645\u06a9 \u0645\u06cc\u200c\u06a9\u0646\u0646\u062f.\u00bb\n")
	p.WriteString("- \u0633\u0648\u0627\u0644 \u0645\u062a\u0642\u0627\u0628\u0644 \u0646\u067e\u0631\u0633.\n")
	p.WriteString("- \u067e\u0627\u0633\u062e\u200c\u0647\u0627\u06cc \u062a\u06a9\u0631\u0627\u0631\u06cc \u0646\u062f\u0647.\n")

	if cfg.CustomRules != "" {
		p.WriteString("\n## \u0642\u0648\u0627\u0646\u06cc\u0646 \u0633\u0641\u0627\u0631\u0634\u06cc\n")
		p.WriteString(cfg.CustomRules + "\n")
	}

	if cfg.TrainingData != "" {
		p.WriteString("\n## \u0646\u06a9\u0627\u062a \u0622\u0645\u0648\u0632\u0634\u06cc (\u06cc\u0627\u062f \u0628\u06af\u06cc\u0631 \u0648 \u0627\u0639\u0645\u0627\u0644 \u06a9\u0646)\n")
		p.WriteString(cfg.TrainingData + "\n")
	}

	if cfg.FAQData != "" {
		p.WriteString("\n## \u067e\u0627\u06cc\u06af\u0627\u0647 \u062f\u0627\u0646\u0634 (\u0641\u0642\u0637 \u0628\u0631 \u0627\u0633\u0627\u0633 \u0627\u06cc\u0646\u0647\u0627 \u062c\u0648\u0627\u0628 \u0628\u062f\u0647)\n")
		p.WriteString(cfg.FAQData + "\n")
	}

	return p.String()
}
